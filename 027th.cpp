#include <iostream>
using namespace std;

int main()
{
 cout << "***\n";
 cout << (10 == 10) << "\n";
 cout << (1000 == 100) << "\n";
 cout << "***\n";
 cout << (10 != 10) << "\n";
 cout << (1000 != 100) << "\n";
 cout << "***\n";
 cout << (40 > 18) << "\n";
 cout << (18 > 18) << "\n";
 cout << "***\n";
 cout << (16 < 18) << "\n";
 cout << (18 < 16) << "\n";
 cout << "***\n";
 cout << (10 >= 18) << "\n";
 cout << (1000 >= 100) << "\n";
 cout << "***\n";
 cout << (10 <= 10) << "\n";
 cout << (1000 <= 100) << "\n";
 cout << "***\n";
 return 0;
}