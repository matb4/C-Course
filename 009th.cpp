#include <iostream>
using namespace std;

int main()
{
   int price = 100;
   cout << "Price Is: " << price;
   cout << "\nPrice After Adding 15 Is: " << price + 15;
   cout << "\nPrice After Adding 50 Is: " << price + 50;
   price = 150;
   cout << "\nThe New Price Is: " << price;
   cout << "\n";
   return 0;
}

